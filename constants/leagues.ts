/**
 * Valida a integridade dos dados das ligas
 */
export const validateLeagueData = (): {
  valid: boolean;
  errors: string[];
  warnings: string[];
} => {
  const errors: string[] = [];
  const warnings: string[] = [];

  for (const [country, league] of Object.entries(LEAGUES)) {
    // Verificar se tem pelo menos uma divisão
    if (Object.keys(league.divisions).length === 0) {
      errors.push(`${country}: No divisions defined`);
      continue;
    }

    // Verificar cada divisão
    for (const [divName, teams] of Object.entries(league.divisions)) {
      // Verificar se tem times
      if (teams.length === 0) {
        errors.push(`${country} - ${divName}: No teams defined`);
        continue;
      }

      // Verificar duplicatas
      const teamNames = teams.map((t) => t.name);
      const duplicates = teamNames.filter(
        (name, index) => teamNames.indexOf(name) !== index,
      );
      if (duplicates.length > 0) {
        errors.push(
          `${country} - ${divName}: Duplicate teams: ${duplicates.join(", ")}`,
        );
      }

      // Verificar tiers consistentes
      const tiers = [...new Set(teams.map((t) => t.leagueTier))];
      if (tiers.length > 1) {
        warnings.push(
          `${country} - ${divName}: Mixed tiers: ${tiers.join(", ")}`,
        );
      }

      // Verificar reputações dentro do range esperado
      for (const team of teams) {
        if (team.reputation < 50 || team.reputation > 99) {
          warnings.push(
            `${country} - ${team.name}: Unusual reputation ${team.reputation}`,
          );
        }
      }
    }
  }

  return {
    valid: errors.length === 0,
    errors,
    warnings,
  };
};

// Export default para compatibilidade
export default LEAGUES;
