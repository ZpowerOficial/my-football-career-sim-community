/**
 * Busca um time pelo nome (busca parcial)
 */
export const searchTeamByName = (searchTerm: string): Team[] => {
  const results: Team[] = [];
  const lowerSearch = searchTerm.toLowerCase();

  for (const league of Object.values(LEAGUES)) {
    for (const teams of Object.values(league.divisions)) {
      for (const team of teams) {
        if (team.name.toLowerCase().includes(lowerSearch)) {
          results.push(team);
        }
      }
    }
  }

  return results;
};

/**
 * Obtém estatísticas gerais das ligas
 */
export const getLeagueStats = (): {
  totalCountries: number;
  totalTeams: number;
  avgTeamsPerCountry: number;
  countriesByReputation: { country: string; reputation: number }[];
} => {
  const countries = Object.keys(LEAGUES);
  let totalTeams = 0;
  const countriesWithRep: { country: string; reputation: number }[] = [];

  for (const [country, league] of Object.entries(LEAGUES)) {
    const teams = Object.values(league.divisions).flat();
    totalTeams += teams.length;
    countriesWithRep.push({
      country,
      reputation: calculateLeagueReputation(league),
    });
  }

  countriesWithRep.sort((a, b) => b.reputation - a.reputation);

  return {
    totalCountries: countries.length,
    totalTeams,
    avgTeamsPerCountry: Math.round(totalTeams / countries.length),
    countriesByReputation: countriesWithRep,
  };
};

