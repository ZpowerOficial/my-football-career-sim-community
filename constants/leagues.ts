import { Team } from "../types";

// ==================== TIPOS ====================

export interface LeagueData {
  name?: string;
  salaryMultiplier: number;
  continentalCup: string;
  divisions: Record<string, Team[]>;
}

export type LeaguesType = Record<string, LeagueData>;

// ==================== FUNÇÕES AUXILIARES ====================

/**
 * Calcula a reputação média de uma divisão
 */
export const calculateDivisionReputation = (teams: Team[]): number => {
  if (teams.length === 0) return 0;
  const sum = teams.reduce((acc, team) => acc + team.reputation, 0);
  return Math.round(sum / teams.length);
};

/**
 * Calcula a reputação da liga (média ponderada das divisões, com peso maior para Tier 1)
 */
export const calculateLeagueReputation = (league: LeagueData): number => {
  const divisions = Object.values(league.divisions);
  if (divisions.length === 0) return 0;

  let totalWeight = 0;
  let weightedSum = 0;

  divisions.forEach((teams, index) => {
    const weight = Math.max(1, 4 - index); // Tier 1 = peso 4, Tier 2 = peso 3, etc.
    const divisionRep = calculateDivisionReputation(teams);
    weightedSum += divisionRep * weight;
    totalWeight += weight;
  });

  return Math.round(weightedSum / totalWeight);
};

/**
 * Busca times por tier da liga (resolve o problema de indexação por string)
 */
export const getTeamsByTier = (league: LeagueData, tier: number): Team[] => {
  for (const divisionName of Object.keys(league.divisions)) {
    const teams = league.divisions[divisionName];
    if (teams.length > 0 && teams[0].leagueTier === tier) {
      return teams;
    }
  }
  return [];
};

/**
 * Busca todos os times de um país
 */
export const getAllTeamsFromCountry = (country: string): Team[] => {
  const league = LEAGUES[country];
  if (!league) return [];

  return Object.values(league.divisions).flat();
};

/**
 * Encontra a liga de um time pelo nome
 */
export const findTeamLeague = (
  teamName: string,
): { country: string; league: LeagueData } | null => {
  for (const [country, league] of Object.entries(LEAGUES)) {
    for (const teams of Object.values(league.divisions)) {
      if (teams.some((t) => t.name === teamName)) {
        return { country, league };
      }
    }
  }
  return null;
};

/**
 * Obtém o nome da competição continental real baseado no país
 */
export const getContinentalCompetitionName = (
  country: string,
): { primary: string; secondary: string } => {
  const europeCountries = [
    "England",
    "Spain",
    "Germany",
    "Italy",
    "France",
    "Netherlands",
    "Portugal",
    "Belgium",
    "Scotland",
    "Switzerland",
    "Austria",
    "Greece",
    "Poland",
    "Ukraine",
    "Russia",
    "Turkey",
  ];

  const southAmericaCountries = ["Brazil", "Argentina", "Colombia", "Uruguay"];
  const asiaCountries = [
    "Saudi Arabia",
    "Japan",
    "South Korea",
    "China",
    "India",
  ];
  const africaCountries = ["Egypt", "Morocco"];
  const concacafCountries = ["USA", "Mexico"];
  const oceaniaCountries = ["Australia"];

  if (europeCountries.includes(country)) {
    return {
      primary: "trophy.championsLeague",
      secondary: "trophy.europaLeague",
    };
  }
  if (southAmericaCountries.includes(country)) {
    return {
      primary: "trophy.libertadores",
      secondary: "trophy.copaSudamericana",
    };
  }
  if (asiaCountries.includes(country) || oceaniaCountries.includes(country)) {
    return { primary: "trophy.afcChampionsLeague", secondary: "trophy.afcCup" };
  }
  if (africaCountries.includes(country)) {
    return {
      primary: "trophy.cafChampionsLeague",
      secondary: "trophy.cafConfederationCup",
    };
  }
  if (concacafCountries.includes(country)) {
    return {
      primary: "trophy.concacafChampionsCup",
      secondary: "trophy.concacafLeague",
    };
  }

  return {
    primary: "trophy.continentalCup",
    secondary: "trophy.continentalCup",
  };
};

