/**
 * Obtém informações completas sobre uma liga
 */
export const getLeagueInfo = (
  country: string,
): {
  league: LeagueData | null;
  reputation: number;
  tier1Teams: Team[];
  totalTeams: number;
  continentalCompetitions: { primary: string; secondary: string };
} => {
  const league = LEAGUES[country];

  if (!league) {
    return {
      league: null,
      reputation: 0,
      tier1Teams: [],
      totalTeams: 0,
      continentalCompetitions: { primary: "", secondary: "" },
    };
  }

  const tier1Teams = getTeamsByTier(league, 1);
  const totalTeams = Object.values(league.divisions).flat().length;
  const reputation = calculateLeagueReputation(league);
  const continentalCompetitions = getContinentalCompetitionName(country);

  return {
    league,
    reputation,
    tier1Teams,
    totalTeams,
    continentalCompetitions,
  };
};

/**
 * Encontra times rivais (mesma liga, reputação similar)
 */
export const findRivalTeams = (
  team: Team,
  reputationRange: number = 5,
): Team[] => {
  const leagueInfo = findTeamLeague(team.name);
  if (!leagueInfo) return [];

  const sameLeagueTeams = getTeamsByTier(leagueInfo.league, team.leagueTier);

  return sameLeagueTeams.filter(
    (t) =>
      t.name !== team.name &&
      Math.abs(t.reputation - team.reputation) <= reputationRange,
  );
};

/**
 * Conta times mais fortes na mesma divisão
 */
export const countStrongerTeams = (team: Team): number => {
  const leagueInfo = findTeamLeague(team.name);
  if (!leagueInfo) return 0;

  const sameLeagueTeams = getTeamsByTier(leagueInfo.league, team.leagueTier);

  return sameLeagueTeams.filter((t) => t.reputation > team.reputation).length;
};

/**
 * Determina a posição estimada do time na liga
 */
export const estimateLeaguePosition = (
  team: Team,
): {
  position: number;
  total: number;
  category:
    | "Title Contender"
    | "Top Half"
    | "Mid Table"
    | "Bottom Half"
    | "Relegation Battle";
} => {
  const leagueInfo = findTeamLeague(team.name);
  if (!leagueInfo) {
    return { position: 1, total: 1, category: "Mid Table" };
  }

  const sameLeagueTeams = getTeamsByTier(leagueInfo.league, team.leagueTier);
  const sortedTeams = [...sameLeagueTeams].sort(
    (a, b) => b.reputation - a.reputation,
  );
  const position = sortedTeams.findIndex((t) => t.name === team.name) + 1;
  const total = sortedTeams.length;
  const percentile = position / total;

  let category:
    | "Title Contender"
    | "Top Half"
    | "Mid Table"
    | "Bottom Half"
    | "Relegation Battle";

  if (percentile <= 0.15) {
    category = "Title Contender";
  } else if (percentile <= 0.35) {
    category = "Top Half";
  } else if (percentile <= 0.65) {
    category = "Mid Table";
  } else if (percentile <= 0.85) {
    category = "Bottom Half";
  } else {
    category = "Relegation Battle";
  }

  return { position, total, category };
};

/**
 * Verifica se o time está qualificado para competições continentais
 */
export const checkContinentalQualification = (
  team: Team,
): {
  qualified: boolean;
  competition: string | null;
  isPrimary: boolean;
} => {
  if (team.leagueTier !== 1) {
    return { qualified: false, competition: null, isPrimary: false };
  }

  const leagueInfo = findTeamLeague(team.name);
  if (!leagueInfo) {
    return { qualified: false, competition: null, isPrimary: false };
  }

  const { position } = estimateLeaguePosition(team);
  const competitions = getContinentalCompetitionName(leagueInfo.country);

  // Top 4 vai para competição principal
  if (position <= 4) {
    return {
      qualified: true,
      competition: competitions.primary,
      isPrimary: true,
    };
  }

  // 5º-7º vai para competição secundária
  if (position <= 7) {
    return {
      qualified: true,
      competition: competitions.secondary,
      isPrimary: false,
    };
  }

  return { qualified: false, competition: null, isPrimary: false };
};

